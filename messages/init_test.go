package messages

import (
	"bufio"
	"bytes"
	"testing"
)

func TestInitWithCreds(t *testing.T) {
	msg := Init("MyClient/1.0", "neo4j", "secret")

	var buf bytes.Buffer
	w := bufio.NewWriter(&buf)

	if err := msg.Serialize(w); err != nil {
		t.Error(err)
		return
	}

	w.Flush()

	expected := []byte{0x00, 0x40, 0xB2, 0x01, 0x8C, 0x4D, 0x79, 0x43, 0x6C, 0x69, 0x65, 0x6E, 0x74, 0x2F, 0x31, 0x2E, 0x30, 0xA3, 0x86, 0x73, 0x63, 0x68, 0x65, 0x6D, 0x65, 0x85, 0x62, 0x61, 0x73, 0x69, 0x63, 0x89, 0x70, 0x72, 0x69, 0x6E, 0x63, 0x69, 0x70, 0x61, 0x6C, 0x85, 0x6E, 0x65, 0x6F, 0x34, 0x6A, 0x8B, 0x63, 0x72, 0x65, 0x64, 0x65, 0x6E, 0x74, 0x69, 0x61, 0x6C, 0x73, 0x86, 0x73, 0x65, 0x63, 0x72, 0x65, 0x74, 0x00, 0x00}
	if v := buf.Bytes(); !bytes.Equal(v, expected) {
		t.Errorf("expected % X, got % X", expected, v)
	}
}

